function comparar(numero1,numero2) {
    if (numero1 > numero2) {
        console.log("O primeiro número é maior que o segundo.")
    } else if (numero1 < numero2) {
        console.log("O primeiro número é menor que o segundo.")
    } else {
        console.log("Os dois números são iguais.");
    }
}

comparar(10, 5);

comparar(3, 8);

comparar(7, 7);